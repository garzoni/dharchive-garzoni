            <p>The <em>Garzoni</em> project undertakes the study of Apprenticeship, Work and Society (GAWS) in the Early Modern Venice by exploiting a specific archival source, namely the <em>Accordi dei Garzoni</em>. This document series from the Venetian State Archive consists in ca. 55.000 apprenticeship contracts registered between 1573 and 1772. These contracts provide precise and detailed information about apprentice employment, including the committed persons (typically a Master, an Apprentice, and a Guarantor), the apprenticeship itself (duration, profession, salary and working conditions) and the workshop. </p>
            <p>The project involves an interdisciplinary team and revolves around two main phases. The first one is dedicated to the building of an information system in order to collect, store, process and render information from the <em>Accordi dei Garzoni</em>. The second phase is devoted to historical studies and investigates new research questions such as the economy of apprenticeship, the transmission of skills, the role of family and the impact of gender, as well as worker migration flows.</p>
            <p>This website is the web-based annotation and exploration interface developed by EPFL-DHLAB, for more information about the project in general please visit this <a href='https://garzoni.hypotheses.org/'>blog</a>.</p>
            <p><strong>Funding</strong>: The project was co-funded by the <a href="https://anr.fr/">French National Research Agency</a> (ANR) and the <a href="http://www.snf.ch/">Swiss National Science Fund</a> (SNSF).</p>
            <p><strong>Time-frame</strong>: 2015-2018</p>

